document.addEventListener('DOMContentLoaded', () => {
    // Select all the language selectors
    const selectors = document.querySelectorAll('select');
    let langSelectors = [];
    selectors.forEach(select => {
        if(select.innerHTML.includes('العربية')) {
            langSelectors.push(select);
        }
    });

    if (langSelectors.length === 0) return;

    // Store original text for all elements that have text nodes
    const walkDOM = (node, func) => {
        if (node.nodeType === 3) { // Text node
            const text = node.nodeValue.trim();
            if (text) {
                func(node);
            }
        } else if (node.nodeType === 1 && node.nodeName !== 'SCRIPT' && node.nodeName !== 'STYLE' && node.nodeName !== 'SELECT') {
            for (let i = 0; i < node.childNodes.length; i++) {
                walkDOM(node.childNodes[i], func);
            }
        }
    };

    const textNodes = [];
    walkDOM(document.body, (node) => {
        textNodes.push({
            node: node,
            original: node.nodeValue
        });
    });

    langSelectors.forEach(langSelect => {
        langSelect.addEventListener('change', (e) => {
            const lang = e.target.value;
            const dict = translations[lang];
            
            // Sync all other selectors
            langSelectors.forEach(sel => {
                if(sel !== e.target) sel.value = lang;
            });

            if (lang === 'en') {
                document.documentElement.dir = 'ltr';
                textNodes.forEach(item => {
                    item.node.nodeValue = item.original;
                });
            } else if (dict) {
                document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
                
                textNodes.forEach(item => {
                    const orig = item.original.trim();
                    if (dict[orig]) {
                        item.node.nodeValue = item.original.replace(orig, dict[orig]);
                    }
                });
            }
        });
    });
});
